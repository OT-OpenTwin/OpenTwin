ot::BlockNetworkEditor::BlockNetworkEditor() {
	m_network = new BlockNetwork;
	setScene(m_network);

	setDragMode(QGraphicsView::DragMode::ScrollHandDrag);
}

ot::BlockNetworkEditor::~BlockNetworkEditor() {
	delete m_network;
}

// ########################################################################################################

void ot::BlockNetworkEditor::mousePressedMoveEvent(QMouseEvent* _event) {
	m_network->update();
}
